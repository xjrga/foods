    public final Interface_dri find(String lifestage_label) {
        Interface_dri found = null;
        Iterator<Interface_dri> it = category.iterator();
        while (it.hasNext()) {
            Interface_dri next = it.next();
            if (next.get_lifestage_label().equals(lifestage_label)) {
                found = next;
                break;
            }
        }
        return found;
    }

    public final void add(Interface_dri dri) {
        category.add(dri);
    }

    public final void remove(String lifestage_label) {
        Interface_dri dri = find(lifestage_label);
        if (dri != null) {
            category.remove(dri);
        }
    }

